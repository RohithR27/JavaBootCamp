public class swapnumbers{
    public static void main(String[] args) {
        int a = 45;
        int b = 23;
        System.out.println("Before Swaping : \n a = "+a+" \n b = "+b);
        //swaping of 2 numbers
        int temp = a;
        a = b;
        b = temp;
        System.out.println("After Swaping : \n a = "+a+" \n b = "+b);
        

    }
}